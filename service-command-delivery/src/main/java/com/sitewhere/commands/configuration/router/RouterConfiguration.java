package com.sitewhere.commands.configuration.router;

import com.fasterxml.jackson.databind.JsonNode;
import com.sitewhere.microservice.configuration.json.JsonConfiguration;
import com.sitewhere.spi.SiteWhereException;
import com.sitewhere.spi.microservice.lifecycle.ITenantEngineLifecycleComponent;

/**
 * Base class for common command router configuration.
 */
public abstract class RouterConfiguration extends JsonConfiguration {

    public RouterConfiguration(ITenantEngineLifecycleComponent component) {
	super(component);
    }

    public void apply(RouterGenericConfiguration configuration) throws SiteWhereException {
	loadFrom(configuration.getConfiguration());
    }

    /**
     * Load subclass from JSON.
     * 
     * @param json
     * @throws SiteWhereException
     */
    public abstract void loadFrom(JsonNode json) throws SiteWhereException;
}
